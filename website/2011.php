Liquid Fluoride Thorium Reactor - FAQ highlights from <a href="http://energyfromthorium.com/faq/">EnergyFromThorium.com</a>.

<h3>Q: What is thorium and what makes it special?</h3>
<p>A: Thorium is a naturally-occuring mineral that holds large amounts of releasable nuclear energy, similar to uranium. This nuclear energy can be released in a special nuclear reactor designed to use thorium. Thorium is special because it is easier to extract this energy completely than uranium due to some of the chemical and nuclear properties of thorium.</p>

<h3>Q: What is a liquid-fluoride reactor?</h3>
<p>A: A liquid-fluoride nuclear reactor is different than conventional nuclear reactors that use solid fuel elements. A liquid-fluoride reactor uses a solution of several fluoride salts, typically lithium fluoride, beryllium fluoride, and uranium tetrafluoride, as its basic nuclear fuel. The fluoride salts have a number of advantages over solid fuels. They are impervious to radiation damage, they can be chemically processed in the form that they are in, and they have a high capacity to hold thermal energy (heat). Additional nuclear fuel can be added or withdrawn from the salt solution during normal operation.</p>

<h3>Q: Are the salts safe?</h3>
<p>A: Very safe. Unlike other coolants considered for high-performance reactors (like liquid sodium) the salts will not react dangerously with air or water. This is because they are already in their most stable chemical form. Their properties do not change even under intense radiation, unlike all solid forms of nuclear fuel.</p>

<h3>Q: What is nuclear waste and how does a liquid-fluoride reactor address this issue?</h3>
<p>A: So-called "nuclear waste" or spent-nuclear fuel is produced in conventional (solid-core) nuclear reactors because they are unable to extract all of the nuclear energy from their fuel before they have to shutdown. LFTR addresses this issue by using a form of nuclear fuel (liquid-fluoride salts of thorium) that allow complete extraction of nuclear energy from the fuel.</p>
