<div class="text">
Helpful tips:
<ul>
<li>Your remix can't be longer than 15 minutes (unless you have a special YouTube account).</li>
<li>Be sure to trim at least a few frames off every piece of footage you use. Otherwise parts may go missing. For example, be sure to take a few frames off my end title card, if you decide to use it.</li>
<li>The trimming won't preview your work if the segment is over 15 minutes in length. Trim a clip way down, close the clip, then re-open it. The editor will then show you a preview as you work.</li>
<li>It is much easier to make editing decisions before using the online editor. Jot down timecodes on paper while watching the video normally. This may be as simple as single start time, and a single end time. Just the same, it is much easier to determine these timecodes outside the editor.</li>
<li>Because THORIUM REMIX 2011 is a 2 hour YouTube video you may find the editor's interface imprecise when scrolling to find in &amp; out points. Click on the top or bottom of the in/out markers to fine-tune your edit points. (Yes... that may take an awful lot of clicking.)</li>
<li>Keep your in/out timecode notes until YouTube has finished rendering your video (which may take a full day). It is much quicker to try again if anything goes wrong if you know exactly what your edits are.</li>
<li>Please share any custom videos you've created with me. I'd like to organize a catalog of LFTR videos for all the demographics we can think up.</li>
</ul>
