</div>

<div class="space">&nbsp;</div>

<div class="text">
In many respects this is similar to how TR2011 was shot... pre-existing events (TEDxYYC &amp; TEAC3) were captured, and interviews were conducted whenever possible. Much TR2012 footage was shot right-before, during &amp; after <a href="http://www.thoriumenergyalliance.com/">TEAC4</a>, as that's the one time per year all the thorium experts meet in one place. Footage includes interviews, tours and (yes) lectures. 
</div>

<div class="space">&nbsp;</div>

<div class="text">
So far we've gotten a very good bang-for-the-buck. I'm the bottleneck right now, as I process footage for use in TR2012 and also release <a href="https://www.youtube.com/user/thoriumremix/videos" title="thorium footage">semi-raw footage</a> and <a href="https://www.youtube.com/user/gordonmcdowell/videos" title="thorium videos">short public videos</a> as I work my way through applicable events.
</div>

<div class="space">&nbsp;</div>

<div class="text">
If you'd like to help ensure TR2012 is the best it can be, paying for a copy of TR2011 certainly helps. It is available as a <a href="http://thoriumremix.com/2012/#download" title="Thorium Remix download">DRM-free digital download</a>, as an <a href="http://www.amazon.com/Thorium-Remix-2011-Kirk-Sorensen/dp/B005OZVX8O/" title="thorium video via Amazon">Amazon Video On Demand</a> title, or (my personal favorite) <a href="http://click.linksynergy.com/fs-bin/stat?id=v5NnabnFHQE&offerid=146261&type=3&subid=0&tmpid=1826&RD_PARM1=http%253A%252F%252Fitunes.apple.com%252Fus%252Fmovie%252Fthorium-remix-2011%252Fid543737235%253Fuo%253D4%2526partnerId%253D30">from iTunes for your iPad and/or Apple TV</a>.
</div>

<div class="space">&nbsp;</div>

<div class="text">
TR2011 is currently seen ~500/day on YouTube. It <a href="http://kunaki.com/sales.asp?PID=PX00ZG2ZI8&pp=1">circulates in public libraries</a>. It occasionally airs on community access TV stations. <strong>That's not nearly good enough</strong> to change public policy. A <strong>better</strong>, <strong>more viral</strong> video is needed. Let's call <a href="http://www.youtube.com/watch?v=WWUeBSoEnRk">TR2009</a>, <a href="http://www.youtube.com/watch?v=P9M__yYbsZ4">TR2011</a> (and my work on <a href="http://www.ted.com/talks/kirk_sorensen_thorium_an_alternative_nuclear_fuel.html">Kirk's TEDxYYC Talk</a>) progressively-<strong>better</strong>, extremely <strong>popular</strong> <a href="http://energyfromthorium.com/2011/06/04/adventures-with-gordon/">learning experiences</a>! How is THORIUM REMIX being improved?
<ul>
<li style="padding-right:10px;">Will <strong>not</strong> consist primarily of lecture footage. This is a show-stopper for most digitual distribution networks. Basically, if your lecture documentary <a href="http://en.wikipedia.org/wiki/An_Inconvenient_Truth">does not contain Al Gore</a>, you are out of luck. Person-to-person interactions and person-to-camera interviews must be the primary mechanism for explaining LFTR.<br />&nbsp;</li>
<li style="padding-right:10px;">Will have a <strong>brilliant opening-credit sequence</strong>. I think TR2011 has a <strong>good</strong> sequence. Good enough for most purposes. But to go insanely viral, the first 10 minutes must be <strong>absolutely riveting</strong>. TR2011 was as good as I can personally make opening credits, I need to pay someone to do this right (and that includes music).<br />&nbsp;</li>
<li style="padding-right:10px;"><strong>Core educational message</strong> will be <stong>made more accessible</strong> to a wider audience. TR2011's educational material is mostly culled from <a href="http://www.youtube.com/watch?v=YVSmf_qmkbg">Kirk Sorensen's Calgary Protospace lecture</a>. One lecture. Kirk's first delivery of it. In a poorly-lit basement. Even having a second delivery of the same material recorded would be invaluable. And we can do much more than that.</li>
</ul> 
...We have already shot enough footage to greatly improve Thorium Remix. How much better <strong>can</strong> it get?<br /><br />

<strong>That's up to you.</strong>
