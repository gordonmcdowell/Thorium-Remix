<div class="text">
I expected that high quality LFTR videos would start popping up on YouTube, given the enthusiastic response to "LFTR in 16 Minutes" and the obvious importance of the subject. Videos did appear, but the capture quality was no better than the Google Tech Talks. Again, nothing a casual viewer would sit through. And "LFTR in 16 Minutes" could only appeal to a narrow audience, given its video quality and jump cuts.</div>

<div class="space">&nbsp;</div>

<div class="text">
It wasn't until April 2011 that there was an opportunity to shoot my own footage: Since "LFTR in 16 Minutes" I'd been volunteering for <a href="http://www.tedxyyc.com/">TEDxYYC</a>, <a href="http://tedxcalgary.ca/">TEDxCalgary</a> and <a href="http://www.youtube.com/watch?v=_qgUMxI3aiM">other Calgary forums</a>, asking them all to please host a Kirk Sorensen lecture on the Liquid Fluoride Thorium Reactor. TEDxYYC 2011 agreed, and was generous enough to fly Kirk in for three days.</div>

<div class="space">&nbsp;</div>

