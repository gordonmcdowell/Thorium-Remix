<div class="text">
Hoping to iterate on "LFTR in 16 Minutes" (which consisted of multiple lectures) and "An Inconvenient Truth" (great example of lecture footage presented in a narrative framework), asked Mount Royal University to host a LFTR talk.</div>

<div class="space">&nbsp;</div>

