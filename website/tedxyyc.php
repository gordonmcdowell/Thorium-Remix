<div class="space">&nbsp;</div>

<div class="text">
Lectures and interviews took place over Kirk's three day visit to Calgary. Often while driving. <a href="http://www.facebook.com/profile.php?id=100000661191367">Chelsea Pratchett</a> helped videotape during the entire three day period.</div>

<div class="space">&nbsp;</div>

<div class="text">
The three Calgary talks offered a fantastic variety in subject matter: TEDxYYC was high-level and aimed at a general audience. MRU was for students studying the intersection of politics and nuclear power (with Kirk taking great questions). PROTOSPACE was an extremely technical crowd, continuing the conversation beyond Kirk's 2.5 hour presentation.</div>

<div class="space">&nbsp;</div>

<div class="text">Kirk's TEDxYYC video was our best shot at quickly getting the  LFTR in front of a large audience. Despite having 10,000 views (now 30,000), <a href="http://www.ted.com/conversations/35/which_tedx_talks_would_you_rec.html">TED Talks did not include Kirk's TEDxYYC talk</a> on their <a href="http://www.ted.com/">TED.com</a> collection of featured talks. This was a bit of a dissapointment, since <a href="http://www.youtube.com/watch?v=py1RrSdVt5A">an (excellent) 2010 TEDxYYC persentation by Ben Cameron</a> was featured after receiving <a href="http://www.youtube.com/watch?v=jl9OiVbmQUA"> 3,000 views</a>. Being <a href="http://www.ted.com/talks/ben_cameron_tedxyyc.html">featured on TED.com</a> gave Ben's presentation a boost from 3,000 views to 150,000 view. Such a multiplier would have been nice.</div>

