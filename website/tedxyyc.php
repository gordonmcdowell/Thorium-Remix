<div class="text">
Calgary's hackerspace, PROTOSPACE was also willing to host a Kirk Sorensen talk on Liquid Fluoride Thorium Reactors.</div>

<div class="space">&nbsp;</div>

<div class="video">
<iframe width="640" height="390" src="http://www.youtube.com/embed/YVSmf_qmkbg" frameborder="0" allowfullscreen></iframe>
